async def search_similar_vehicles(specs: Dict[str, Any]) -> Dict[str, Any]:
    """
    Search for similar UAV/VTOL configurations and their PID tuning setups.
    Targets both PX4 and ArduPilot documentation.
    """
    vt_type_val = specs.get("VT_TYPE", "")
    try:
        vt_type_int = int(vt_type_val)
    except (ValueError, TypeError):
        vt_type_int = None

    if vt_type_int == 2 or str(vt_type_val).lower() == "standard vtol quadplane":
        vtype_desc = "standard VTOL quadplane"
    elif vt_type_int == 1:
        vtype_desc = "tiltrotor VTOL"
    elif vt_type_int == 0:
        vtype_desc = "tailsitter VTOL"
    else:
        vtype_desc = str(vt_type_val) or "multirotor"

    weight = specs.get("weight") or specs.get("WEIGHT_BASE") or specs.get("WEIGHT_GROSS") or "unknown"
    rotor_count = specs.get("CA_ROTOR_COUNT") or specs.get("rotor_count", "")
    wingspan = specs.get("FW_WING_SPAN") or specs.get("wingspan", "")
    
    spec_desc = f"a UAV of type: {vtype_desc}"
    if weight != "unknown":
        spec_desc += f", weighing {weight} kg"
    if rotor_count:
        spec_desc += f", with {rotor_count} rotors"
    if wingspan:
        spec_desc += f", wingspan {wingspan}m"

    prompt = (
        f"Search for PX4 AND ArduPilot tuning guides, forum threads, or technical specifications for {spec_desc}. "
        "Include the ArduPilot QuadPlane VTOL tuning process (ardupilot.org/plane/docs/quadplane-vtol-tuning-process.html) "
        "and PX4 Multicopter PID Tuning Guide (docs.px4.io). "
        "Find standard PID values, common control issues (vibration, motor spread, CoG offset), recommended "
        "notch filter configurations, and the correct tuning sequence (vibration filtering → rate PID → attitude PID). "
        "Include specific configurations and numerical values from similar airframes when available. "
        "Also search for discuss.ardupilot.org and discuss.px4.io forum posts about similar weight-class VTOL tuning. "
        "Provide the final synthesized response summary in Turkish language. (Lütfen bulduğun tüm araştırma sonuçlarını ve bilgileri Türkçe olarak özetle.)"
    )
    
    return await query_gemini_with_search(prompt)

