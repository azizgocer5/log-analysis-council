async def query_gemini_with_search(prompt: str, model: str = "gemini-3.5-flash") -> Dict[str, Any]:
    """
    Query Gemini 1.5 with Google Search Grounding enabled.
    Returns a dictionary containing:
      - 'answer': The textual response generated by the model.
      - 'sources': A list of grounding sources (dict with 'title' and 'url').
      - 'queries': The search queries generated/used by the model.
    """
    print(f"[Web Search] Querying {model} with prompt: {prompt[:180]}...")
    # Use the key from config
    api_key = OPENROUTER_API_KEY
    if not api_key:
        print("[Web Search] No API key configured. Returning empty search results.")
        return {
            "answer": "No API key configured for Web Search.",
            "sources": [],
            "queries": []
        }

    # API endpoint for content generation
    url = f"https://generativelanguage.googleapis.com/v1beta/models/{model}:generateContent?key={api_key}"
    
    payload = {
        "contents": [
            {
                "parts": [
                    {"text": prompt}
                ]
            }
        ],
        "tools": [
            {
                "google_search": {}
            }
        ]
    }

    headers = {
        "Content-Type": "application/json"
    }

    max_retries = 3
    base_delay = 5.0

    for attempt in range(max_retries):
        try:
            async with httpx.AsyncClient(timeout=120.0) as client:
                response = await client.post(url, headers=headers, json=payload)
                
                # Check for rate limit
                if response.status_code == 429:
                    print(f"[Web Search] Rate limited (429) on attempt {attempt + 1}. Retrying...")
                    if attempt < max_retries - 1:
                        await asyncio.sleep(base_delay * (2 ** attempt))
                        continue
                
                response.raise_for_status()
                data = response.json()
                
                candidates = data.get("candidates", [])
                if not candidates:
                    return {
                        "answer": "No candidates returned from Gemini.",
                        "sources": [],
                        "queries": []
                    }
                
                candidate = candidates[0]
                content = candidate.get("content", {})
                parts = content.get("parts", [])
                
                answer = ""
                if parts:
                    answer = parts[0].get("text", "")
                
                # Parse grounding metadata
                grounding_metadata = candidate.get("groundingMetadata", {})
                queries = grounding_metadata.get("webSearchQueries", [])
                
                # Extract grounding chunks
                grounding_chunks = grounding_metadata.get("groundingChunks", [])
                sources = []
                seen_urls = set()
                
                for chunk in grounding_chunks:
                    web = chunk.get("web", {})
                    title = web.get("title", "")
                    uri = web.get("uri", "")
                    if uri and uri not in seen_urls:
                        seen_urls.add(uri)
                        sources.append({
                            "title": title or uri,
                            "url": uri
                        })
                
                return {
                    "answer": answer,
                    "sources": sources,
                    "queries": queries
                }

        except httpx.HTTPStatusError as e:
            body = e.response.text.replace(api_key, "***REDACTED***")
            print(f"[Web Search] HTTP {e.response.status_code} error: {body}")
            if attempt < max_retries - 1:
                await asyncio.sleep(base_delay * (2 ** attempt))
            else:
                print(f"[Web Search] All retries failed with HTTP error. Using fallback database content.")
                return get_fallback_search_result(prompt)
        except Exception as e:
            # Mask API key in error messages
            error_desc = f"{type(e).__name__}: {str(e)}" if str(e) else type(e).__name__
            safe_error = error_desc.replace(api_key, "***REDACTED***") if api_key else error_desc
            print(f"[Web Search] Exception querying Gemini Search (attempt {attempt + 1}): {safe_error}")
            if attempt < max_retries - 1:
                await asyncio.sleep(base_delay * (2 ** attempt))
            else:
                print(f"[Web Search] All retries failed with exception. Using fallback database content.")
                return get_fallback_search_result(prompt)

    print(f"[Web Search] Search timed out after {max_retries} attempts. Using fallback database content.")
    return get_fallback_search_result(prompt)


